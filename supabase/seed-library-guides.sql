update resources set body_md = $g$Fundraising is not a season or a sprint. It is a rhythm you hold every week, and the rhythm is what compounds.

## What a week looks like
- One planning block to start the week: thirty minutes with your pipeline. Who needs a touch, what is due, what moved.
- Two to three touch blocks midweek: calls, notes, coffees, thank yous. Protect these like program time, because they are program time for the organization's future.
- One wrap block to close the week: log what happened, set next steps, flag anything stuck.

## The rules that make it hold
1. The blocks live on your calendar with names, not intentions. "Donor touches, Tuesday 10 to 11" survives a busy week. "Do fundraising more" does not.
2. Small and steady beats big and rare. Three real touches a week is over a hundred fifty a year. That is a fundraising program.
3. When a week collapses, and some will, hold the planning block anyway. Fifteen minutes with the pipeline keeps the thread. You can skip the touches once. You cannot skip knowing where things stand.

## Weekly pitch practice
Five minutes, every week, from week one: pitch the work to each other. Before the deck exists, pitch without it. The reps are the point. By the season, the story tells itself.

## The test
If someone asked "what did fundraising look like last week," you should be able to answer in one minute from the log. If you cannot, the rhythm is not holding yet. Tighten the blocks, not the ambition.$g$
